from enum import Enum

class AnonReqType(Enum):
    REGIONS = 0x01
    OWNER = 0x02
    BASIC = 0x03 # just remote clock

class BinaryReqType(Enum):
    STATUS = 0x01
    KEEP_ALIVE = 0x02
    TELEMETRY = 0x03
    MMA = 0x04
    ACL = 0x05
    NEIGHBOURS = 0x06

class ControlType(Enum):
    NODE_DISCOVER_REQ = 0x80
    NODE_DISCOVER_RESP = 0x90

